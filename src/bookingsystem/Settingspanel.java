    public void adminLogin(){
        String message;
        message = "Enter Admin Passowrd";
        Object adminPass = null;
        try{
            do{
                adminPass = JOptionPane.showInputDialog(null, message, "GBHotel Admin",JOptionPane.QUESTION_MESSAGE, new ImageIcon("C:\\Users\\Keth Dominic\\Documents\\NetBeansProjects\\BookingSystem\\src\\bookingsystem\\images\\icons8_administrator_male_30px.png"),null, null);
                message = "Wrong Password";
                if(adminPass.equals("admin")){
                    AdminIFrame adminFrame = new AdminIFrame();
                    settingsDPane.removeAll();
                    adminFrame.setVisible(true);
                    settingsDPane.add(adminFrame);
                    isAdmin = true;
                }
            }while(!adminPass.equals("admin"));
        }
        catch (Exception e){    
        }
    }
